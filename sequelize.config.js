// Configuration de Sequelize:

import { Sequelize } from 'sequelize';

// On paramètre sequelize avec, par ordre dans la parenthése, le nom de la bdd, le username de la bdd et son password:
const sequelize = new Sequelize('santiago', 'santiago', 'santiago', {
    host: 'localhost', // On définit l'adresse du serveur de bdd.
    dialect: 'postgres', // On indique le type de base de données que Sequelize doit utiliser.
    logging: false, // Désactive les consoles logs spécifiques à SQL si besoin.
});

export default sequelize;
